# 복합 대입 연산자 : +=, -=, *=, /=, %=, **= (숫자 덧셈,뺄셈,곱셈,나눗셈,나머지,제곱후 대입)
number = 100
number += 10
number += 20
number += 30
print("number :",number)

# 문자열 복합 대입 연산자
string = "안녕하세요"
string += "~"
string += "!"
print("string :",string)