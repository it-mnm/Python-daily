# 문자열 연결 연산자 사용
print("3 + 4 = " + str(3 + 4))     # 3 + 4 = 7

# 이렇게 표현식 여러개를 사용할 때마다 str() 함수로 감싸는게 번거로움
# 이럴 때 format() 함수를 사용한다.
print("3 + 4 = {}".format(3 + 4))   # 3 + 4 = 7

# 파이썬 3.6 버전부터는 더 간단한 방법을 제공
# f-문자열
# f"문자열{표현식}문자열" 의 형태로 작성
print("{}".format(10))
print(f'{10}')

# {} 안에 어떤 표현식이든 넣을 수 있음.
print(f"3 + 4 = {3 + 4}")
print(f"""1 + 2 = {1 + 2}
2 + 3 = {2 +3}
3 + 4 = {3 + 4}""")

# f-문자열보다 format() 함수를 사용하는 것이 더 좋은 경우

