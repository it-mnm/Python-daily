# 2. 데이터를 리스트에 담아서 사용할 때
data = ['별', 2, 'M', '서울특별시 강서구', 'Y']

print(f"""이름: {data[0]}
나이: {data[1]}
성별: {data[2]}
지역: {data[3]}
중성화 여부: {data[4]}""")

print("""이름: {}
나이: {}
성별: {}
지역: {}
중성화 여부: {}""".format(*data))   # 전개 연산자를 사용하여 리스트 내용을 전개한다.